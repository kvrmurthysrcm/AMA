def daterange(start_date: date, end_date: date) -> Iterable[date]:
    cur = start_date
    while cur <= end_date:
        yield cur
        cur += timedelta(days=1)


def date_key(d: date) -> int:
    return int(d.strftime("%Y%m%d"))


def sql_quote(value):
    if value is None:
        return "NULL"
    if isinstance(value, bool):
        return "TRUE" if value else "FALSE"
    if isinstance(value, (int, float)):
        return str(value)
    if isinstance(value, date):
        return f"'{value.isoformat()}'"
    s = str(value).replace("'", "''")
    return f"'{s}'"


def clamp(v, lo, hi):
    return max(lo, min(hi, v))


def month_seasonality(d: date) -> float:
    # festive bump in Oct-Dec, mild bump in Jun
    seasonal = {
        1: 0.92, 2: 0.95, 3: 1.00, 4: 0.98, 5: 1.02, 6: 1.08,
        7: 1.00, 8: 1.03, 9: 1.06, 10: 1.18, 11: 1.28, 12: 1.35
    }
    return seasonal.get(d.month, 1.0)


def weekday_factor(d: date) -> float:
    # weekends slightly stronger for electronics retail browsing
    return 1.10 if d.weekday() >= 5 else 1.0


def build_dim_date_rows(start_date: date, end_date: date):
    rows = []
    for d in daterange(start_date, end_date):
        rows.append({
            "date_key": date_key(d),
            "full_date": d,
            "year_num": d.year,
            "quarter_num": ((d.month - 1) // 3) + 1,
            "month_num": d.month,
            "month_name": d.strftime("%B"),
            "week_num": int(d.strftime("%U")),
            "day_num": d.day,
            "day_name": d.strftime("%A"),
            "is_weekend": d.weekday() >= 5,
            "is_month_start": d.day == 1,
            "is_month_end": (d + timedelta(days=1)).month != d.month,
        })
    return rows


