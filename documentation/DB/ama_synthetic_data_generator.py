def write_csv(path: Path, rows: List[dict]):
    if not rows:
        return
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(f, fieldnames=list(rows[0].keys()))
        writer.writeheader()
        writer.writerows(rows)


def insert_statement(table_name: str, rows: List[dict]) -> str:
    if not rows:
        return f"-- No rows for {table_name}\n"
    cols = list(rows[0].keys())
    lines = [f"INSERT INTO {table_name} ({', '.join(cols)}) VALUES"]
    value_lines = []
    for row in rows:
        vals = [sql_quote(row[c]) for c in cols]
        value_lines.append("(" + ", ".join(vals) + ")")
    lines.append(",\n".join(value_lines) + ";\n")
    return "\n".join(lines)


def write_sql(output_path: Path, brands, categories, products, dim_dates, sales, traffic, inventory, finance):
    output_path.parent.mkdir(parents=True, exist_ok=True)

    product_rows = [{
        "product_id": p.product_id,
        "tenant_id": p.tenant_id,
        "account_id": p.account_id,
        "asin": p.asin,
        "sku": p.sku,
        "product_name": p.product_name,
        "short_description": p.short_description,
        "brand_id": p.brand_id,
        "category_id": p.category_id,
        "product_type": p.product_type,
        "price_amount": p.price_amount,
        "currency_code": p.currency_code,
        "rating_avg": p.rating_avg,
        "review_count": p.review_count,
        "is_active": p.is_active,
        "launched_at": p.launched_at,
    } for p in products]

    with output_path.open("w", encoding="utf-8") as f:
        f.write("-- AMA Synthetic Seed Data\n")
        f.write("-- Generated by ama_synthetic_data_generator.py\n\n")
        f.write("BEGIN;\n\n")
        f.write(insert_statement("ama.brand", brands))
        f.write(insert_statement("ama.category", categories))
        f.write(insert_statement("ama.product", product_rows))
        f.write(insert_statement("ama.dim_date", dim_dates))
        f.write(insert_statement("ama.fact_sales_daily", sales))
        f.write(insert_statement("ama.fact_traffic_daily", traffic))
        f.write(insert_statement("ama.fact_inventory_daily", inventory))
        f.write(insert_statement("ama.fact_finance_daily", finance))
        f.write("COMMIT;\n")


