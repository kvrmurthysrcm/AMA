def generate_master_data(
    tenant_id: int,
    account_id: int | None,
    num_products: int,
    seed: int,
    category_mix: Dict[str, float] | None = None,
):
    random.seed(seed)

    category_names = list(CATEGORIES.keys())
    if not category_mix:
        category_mix = {k: 1.0 / len(category_names) for k in category_names}

    # categories
    categories = []
    category_id_map = {}
    next_category_id = tenant_id * 100000 + 1
    for idx, cat_name in enumerate(category_names, start=1):
        category_id = next_category_id + idx - 1
        category_id_map[cat_name] = category_id
        categories.append({
            "category_id": category_id,
            "tenant_id": tenant_id,
            "category_code": cat_name.upper().replace(" ", "_"),
            "category_name": cat_name,
            "parent_category_id": None,
            "category_level": 1,
        })

    # brands
    all_brands = sorted({b for conf in CATEGORIES.values() for b in conf["brands"]})
    brands = []
    brand_id_map = {}
    next_brand_id = tenant_id * 100000 + 1
    for idx, brand_name in enumerate(all_brands, start=1):
        brand_id = next_brand_id + idx - 1
        brand_id_map[brand_name] = brand_id
        brands.append({
            "brand_id": brand_id,
            "tenant_id": tenant_id,
            "brand_code": brand_name.upper().replace(" ", "_"),
            "brand_name": brand_name,
        })

    # products
    products: List[Product] = []
    next_product_id = tenant_id * 1000000 + 1

    weighted_categories = []
    for c, w in category_mix.items():
        weighted_categories.extend([c] * max(1, int(w * 100)))

    for i in range(num_products):
        cat = random.choice(weighted_categories)
        conf = CATEGORIES[cat]
        brand = random.choice(conf["brands"])
        spec = random.choice(SPEC_HINTS[cat])
        adjective = random.choice(ADJECTIVES)
        model_no = f"{random.randint(100,999)}{random.choice(['X','Z','A','P'])}"
        product_name = f"{brand} {adjective} {cat} {model_no} {spec}"
        price = round(random.uniform(*conf["price_range"]), 2)

        product_id = next_product_id + i
        asin = f"B{tenant_id:02d}{product_id:08d}"[:10]
        sku = f"AMA-{tenant_id}-{product_id}"
        launched = date.today() - timedelta(days=random.randint(30, 900))

        products.append(Product(
            product_id=product_id,
            tenant_id=tenant_id,
            account_id=account_id,
            asin=asin,
            sku=sku,
            product_name=product_name,
            short_description=f"{cat} from {brand}, {spec}",
            brand_id=brand_id_map[brand],
            category_id=category_id_map[cat],
            product_type=cat,
            price_amount=price,
            currency_code="INR",
            rating_avg=round(random.uniform(3.6, 4.8), 2),
            review_count=random.randint(0, 2500),
            is_active=True,
            launched_at=launched,
        ))

    return brands, categories, products


def generate_fact_rows(
    tenant_id: int,
    account_id: int | None,
    products: List[Product],
    start_date: date,
    end_date: date,
    seed: int,
    marketplace_code: str = "IN",
):
    random.seed(seed + 999)

    sales_rows = []
    traffic_rows = []
    inventory_rows = []
    finance_rows = []

    for p in products:
        conf = CATEGORIES[p.product_type]
        min_units, max_units = conf["daily_unit_range"]

        # product popularity profile
        popularity = random.uniform(0.4, 2.6)
        # some hero products
        if random.random() < 0.08:
            popularity *= random.uniform(2.0, 4.0)

        inventory_level = random.randint(20, 250)

        for d in daterange(start_date, end_date):
            season = month_seasonality(d)
            weekday = weekday_factor(d)
            trend = 1.0 + ((d - start_date).days / max(1, (end_date - start_date).days)) * random.uniform(-0.15, 0.25)
            noise = random.uniform(0.65, 1.35)

            expected_units = popularity * season * weekday * trend * noise
            expected_units *= random.uniform(min_units + 0.3, max_units + 0.8)
            units_sold = int(max(0, round(expected_units / max_units)))

            # keep realistic
            units_sold = clamp(units_sold, 0, max_units * 3)

            orders_count = max(0, math.ceil(units_sold / random.uniform(1.0, 2.8))) if units_sold > 0 else 0
            gross_sales = round(units_sold * p.price_amount, 2)
            discount = round(gross_sales * random.uniform(0.00, 0.08), 2)
            net_sales = round(gross_sales - discount, 2)
            returned_units = 1 if units_sold > 0 and random.random() < 0.03 else 0
            returned_amount = round(returned_units * p.price_amount * random.uniform(0.8, 1.0), 2)
            avg_price = round(net_sales / units_sold, 2) if units_sold > 0 else p.price_amount

            sessions = int(max(0, units_sold * random.uniform(8, 24) + random.uniform(0, 50)))
            page_views = int(max(sessions, sessions * random.uniform(1.1, 2.8)))
            conversion = round((units_sold / sessions) * 100, 4) if sessions > 0 else 0.0
            buy_box = round(random.uniform(75.0, 99.0), 2)

            opening_stock = inventory_level
            inbound = random.randint(0, 80) if inventory_level < 30 and random.random() < 0.30 else 0
            reserved = random.randint(0, min(5, max(0, opening_stock)))
            damaged = random.randint(0, 2) if random.random() < 0.05 else 0
            closing_stock = max(0, opening_stock + inbound - units_sold - damaged)
            sellable = max(0, closing_stock - reserved)
            avg_daily_sale = max(0.1, popularity * (max_units / 5.0))
            days_of_cover = round(sellable / avg_daily_sale, 2)
            low_stock = sellable < max(5, int(avg_daily_sale * 3))
            out_of_stock = sellable == 0
            inventory_level = closing_stock

            fee_amount = round(net_sales * random.uniform(0.05, 0.09), 2)
            commission_amount = round(net_sales * random.uniform(0.05, 0.12), 2)
            fulfillment_fee = round(units_sold * random.uniform(15, 120), 2) if units_sold > 0 else 0
            storage_fee = round(max(0, closing_stock) * random.uniform(0.3, 2.2), 2)
            refund_amount = round(returned_amount, 2)
            adjustment_amount = round(random.uniform(-50, 50), 2) if random.random() < 0.03 else 0.0
            payout = round(net_sales - fee_amount - commission_amount - fulfillment_fee - storage_fee - refund_amount + adjustment_amount, 2)
            est_profit = round(net_sales * random.uniform(0.06, 0.22) - refund_amount, 2)

            dk = date_key(d)

            sales_rows.append({
                "tenant_id": tenant_id,
                "account_id": account_id,
                "product_id": p.product_id,
                "date_key": dk,
                "marketplace_code": marketplace_code,
                "units_sold": units_sold,
                "orders_count": orders_count,
                "gross_sales_amount": gross_sales,
                "discount_amount": discount,
                "net_sales_amount": net_sales,
                "returned_units": returned_units,
                "returned_amount": returned_amount,
                "avg_selling_price": avg_price,
            })

            traffic_rows.append({
                "tenant_id": tenant_id,
                "account_id": account_id,
                "product_id": p.product_id,
                "date_key": dk,
                "marketplace_code": marketplace_code,
                "sessions_count": sessions,
                "page_views_count": page_views,
                "buy_box_percentage": buy_box,
                "unit_session_percentage": conversion,
                "browser_sessions": int(sessions * random.uniform(0.45, 0.75)),
                "mobile_app_sessions": int(sessions * random.uniform(0.25, 0.55)),
                "browser_page_views": int(page_views * random.uniform(0.45, 0.75)),
                "mobile_app_page_views": int(page_views * random.uniform(0.25, 0.55)),
            })

            inventory_rows.append({
                "tenant_id": tenant_id,
                "account_id": account_id,
                "product_id": p.product_id,
                "date_key": dk,
                "marketplace_code": marketplace_code,
                "opening_stock_qty": opening_stock,
                "closing_stock_qty": closing_stock,
                "inbound_qty": inbound,
                "reserved_qty": reserved,
                "damaged_qty": damaged,
                "sellable_qty": sellable,
                "days_of_cover": days_of_cover,
                "low_stock_flag": low_stock,
                "out_of_stock_flag": out_of_stock,
            })

            finance_rows.append({
                "tenant_id": tenant_id,
                "account_id": account_id,
                "product_id": p.product_id,
                "date_key": dk,
                "marketplace_code": marketplace_code,
                "fee_amount": fee_amount,
                "commission_amount": commission_amount,
                "fulfillment_fee_amount": fulfillment_fee,
                "storage_fee_amount": storage_fee,
                "refund_amount": refund_amount,
                "adjustment_amount": adjustment_amount,
                "payout_amount": payout,
                "estimated_profit_amount": est_profit,
            })

    return sales_rows, traffic_rows, inventory_rows, finance_rows


