def parse_args():
    parser = argparse.ArgumentParser(description="Generate synthetic data for AMA PostgreSQL schema.")
    parser.add_argument("--tenant-id", type=int, required=True, help="Tenant ID to generate data for.")
    parser.add_argument("--account-id", type=int, default=None, help="Optional marketplace account ID.")
    parser.add_argument("--num-products", type=int, default=1000, help="Number of products to generate.")
    parser.add_argument("--months", type=int, default=12, help="Number of months of daily facts to generate.")
    parser.add_argument("--seed", type=int, default=42, help="Random seed for reproducibility.")
    parser.add_argument("--output-format", choices=["sql", "csv"], default="sql", help="Output format.")
    parser.add_argument("--output-dir", default="./ama_synth_out", help="Output directory.")
    parser.add_argument("--marketplace-code", default="IN", help="Marketplace code, e.g. IN, US.")
    return parser.parse_args()


def main():
    args = parse_args()

    output_dir = Path(args.output_dir)
    end_date = date.today()
    start_date = end_date - timedelta(days=args.months * 30)

    brands, categories, products = generate_master_data(
        tenant_id=args.tenant_id,
        account_id=args.account_id,
        num_products=args.num_products,
        seed=args.seed,
    )

    dim_dates = build_dim_date_rows(start_date, end_date)
    sales, traffic, inventory, finance = generate_fact_rows(
        tenant_id=args.tenant_id,
        account_id=args.account_id,
        products=products,
        start_date=start_date,
        end_date=end_date,
        seed=args.seed,
        marketplace_code=args.marketplace_code,
    )

    if args.output_format == "csv":
        write_csv(output_dir / "brand.csv", brands)
        write_csv(output_dir / "category.csv", categories)
        write_csv(output_dir / "product.csv", [{
            "product_id": p.product_id,
            "tenant_id": p.tenant_id,
            "account_id": p.account_id,
            "asin": p.asin,
            "sku": p.sku,
            "product_name": p.product_name,
            "short_description": p.short_description,
            "brand_id": p.brand_id,
            "category_id": p.category_id,
            "product_type": p.product_type,
            "price_amount": p.price_amount,
            "currency_code": p.currency_code,
            "rating_avg": p.rating_avg,
            "review_count": p.review_count,
            "is_active": p.is_active,
            "launched_at": p.launched_at,
        } for p in products])
        write_csv(output_dir / "dim_date.csv", dim_dates)
        write_csv(output_dir / "fact_sales_daily.csv", sales)
        write_csv(output_dir / "fact_traffic_daily.csv", traffic)
        write_csv(output_dir / "fact_inventory_daily.csv", inventory)
        write_csv(output_dir / "fact_finance_daily.csv", finance)
        print(f"CSV files written to: {output_dir}")
    else:
        out_file = output_dir / f"ama_seed_tenant_{args.tenant_id}.sql"
        write_sql(out_file, brands, categories, products, dim_dates, sales, traffic, inventory, finance)
        print(f"SQL seed file written to: {out_file}")


if __name__ == "__main__":
    main()
